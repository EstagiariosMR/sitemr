<h1>História completa</h1>

<h2>Dados bibliográficos da professora Maria Manuela Rocha</h2>

<ul>

 <li><strong>Nome:</strong> Maria Manuela Rocha;</li>
 <li><strong>Data de nascimento:</strong> 23 de novembro de 1904;</li>
 <li><strong>Data de falescimento:</strong> 1° de outubro de 1958;</li>
 <li><strong>Filiação:</strong> Manoel Marques da Rocha e Bertholina Junqueira Rocha.</li>

</ul>

<p>Maria Rocha foi à última filha de uma família de 14 (quatorze) filhos. Sua mãe era professora, e foi com ela que aprendeu as primeiras letras, isto é, lhe ministrou os conhecimentos do Curso Primário. Mais tarde vai interna para o Colégio dos Anjos, em Porto Alegre, para fazer o Curso Complementar. Em 1925, recebeu o diploma de professora.</p>

<p>Logo depois de formada, começou trabalhar no Colégio Elementar, depois Escola Normal "Olavo Bilac". Foi a primeira Assistente de Direção da professora Emília Flores, na Escola Normal e respondeu pela direção do estabelecimento.</p>

<p>Idealista que era, um dia visitou o Presídio Municipal e viu que lá, muito poderia fazer a favor daquelas criaturas. Assim pensando, fundou em 27 de maio de 1955, a Escolinha São Francisco de Assis, que tinha como objetivo alfabetizar e recuperar os presidiários, através de uma terapêutica ocupacional.</p>

<p>Foi uma das principais figuras da comissão organizadora dos festejos do Centenário de Santa Maria, sendo idealizadora de uma festa popular, "A Chuva de Estrelas".</p>

<p>Em 1º de outubro de 1958, às 3 horas e 45 minutos, faleceu em sua residência, vítima de câncer.</p>

<p>Seu corpo foi velado no auditório da Escola Normal "Olavo Bilac". Em ato tocante, a Escola do Presídio Municipal foi levar o derradeiro adeus à sua amada benfeitora.</p>

<p>Sua morte abalou não só o magistério, como também o mundo intelectual e a sociedade Santamarienses. De imediato surgiu entre seus colegas a ideia de dar ao Ginásio da Escola o nome da incomparável mestra, atendendo a um dever da justiça.</p>

<p>Em 28 de dezembro de 1962 nossa Escola passou a denominar-se Colégio Estadual Profª Maria Rocha, hoje E.E.E.M.P. Maria Rocha.</p>

<p>Maria Manuela Rocha está entre nós e estará sempre através da história e de seu lema: “Ama teu próximo mais que a ti mesmo”.</p>

<h2>Histórico e Atos Oficiais</h2>

<p><strong>1)</strong> Decreto-lei de criação n.º 97, de 16 de julho de 1941, com a denominação de Curso Ginasial, anexo à Escola Normal "Olavo Bilac", publicado no Diário Oficial de 17.04.1941.</p>

<p><strong>2)</strong> Decreto n.º 7654 de 08 de fevereiro de 1957, publicado no Diário Oficial de 12.02.1957, que o desanexa da Escola Normal "Olavo Bilac".</p>

<p><strong>3)</strong> Decreto n.º 9365 de 19 de setembro de 1958, publicado no Diário Oficial de 19.09.1958, que passa a ser denominado Ginásio Estadual Caetano Pagliuca.</p>

<p><strong>4)</strong> Decreto n.º 14.010, de 22 de agosto de 1962, publicado no Diário Oficial de 25.08.1962, transformando-o em Colégio Estadual Caetano Pagliuca.</p>

<p><strong>5)</strong> Decreto 14.639 de 28 de dezembro de 1962, passa a ser denominado Colégio Estadual Maria Rocha (consta no Parecer 12/12/80 do Conselho Estadual de Educação).</p>

<p><strong>6)</strong> Decreto n.º 14.768, de 21 de janeiro de 1963, publicado no Diário Oficial de 22.01.1963 alterando o nome para Colégio Estadual Professora Maria Rocha.</p>

<p><strong>7)</strong> Portaria n.º 30.423 de 10 de dezembro de 1979, publicada no Diário Oficial de 14.12.1979, passando a denominar-se Escola Estadual de II Grau Professora Maria Rocha.</p>

<p><strong>8)</strong> Portaria ATO/SE, n.º 00111, de 19 de abril de 2000- de Escola Estadual de 2° Grau Professora Maria Rocha para Escola Estadual de Ensino Médio Professora Maria Rocha.</p>

<p><strong>9)</strong> Curso Técnico de Contabilidade, Subsequente - Parecer n° 934/98 de 14 de outubro de 1998.</p>

<p><strong>10)</strong> Curso Técnico em Secretariado, Subsequente - Parecer n.º 362/99 de 28 de abril de 1999.</p>

