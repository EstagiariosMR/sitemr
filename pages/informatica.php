        <h2>Objetivo do Curso</h2>
        <p>
            O Curso Técnico em Informática oferece formação de nível subsequente e preconiza a formação de profissional
            habilitado para atuar no setor de informática, bem como forma profissionais com competências técnicas para
            ingressar no mundo do trabalho, atualizadas nas bases e formas de gestão da informática, contribuindo de maneira
            significativa para o desenvolvimento humano e sustentável da comunidade.
        </p>

        <h2>Critérios de Avaliação</h2>
        <p>
            A avaliação considerada como processo de construção do conhecimento combina procedimentos de avaliação de
            competências que se realiza de forma contínua, envolvendo teoria e prática. O resultado é expresso pelos pareceres
            Apto e Não Apto. Quando o aluno, após as atividades de recuperação desenvolvidas durante o processo for considerado
            Não Apto, a escola oferece uma nova oportunidade, sendo retomados os conteúdos mínimos que propiciem aquisição de
            conhecimentos básicos para promoção da etapa seguinte: os estudos adicionais. No período de estudos adicionais,
            são retomadas as competências significativas para a promoção do aluno.
        </p>
        <p>
            A Escola exige do aluno o percentual de 75% de frequência nas atividades curriculares, conforme legislação vigente,
            oferecendo-lhe atividades compensatórias e presencias de infrequência.
        </p>

        <h2>Estágio</h2>
        <p>
            O estágio curricular obrigatório oportuniza a vivência da integração entre a teoria e a prática fora do ambiente
            pedagógico. É realizado no quarto semestre do curso, em empresa ou propriedade do setor público ou privado que
            exija ou necessite de profissional na área de Informática. O estágio é realizado sob a orientação do professor da
            disciplina de orientação de Estágio e deve ter a duração de 400 horas.
        </p>

        <h2>Perfil Profissional de Conclusão</h2>
        <p>
            O Técnico em Informática, Eixo Tecnológico Informação e Comunicação da Escola Estadual de Ensino Médio Prof.ª Maria
            Rocha deve ser capaz de dar suporte aos usuários de informática em serviços de montagem e manutenção de computadores,
            em instalação e configuração de redes, de desenvolver banco de dados e programação, desenvolver e atualizar sites,
            instalar e configurar sistemas operacionais e elaborar e avaliar projetos na área de informática, contemplando uma
            visão empreendedora.
        </p>
        <p>
            O Técnico em Informática é um profissional qualificado para compreender e tomar decisões relativas às mais diversas
            áreas de informática. Esse profissional deve demonstrar competências de:
        </p>
        <ul>
            <li>Operar, instalar, configurar e fazer manutenção de sistemas operacionais e aplicativos.</li>
            <li>Construir, instalar e realizar a manutenção de banco de dados.</li>
            <li>Instalar, configurar e fazer manutenção de computadores.</li>
            <li>Construir, instalar e realizar a manutenção de softwares.</li>
            <li>Instalar, configurar e operar redes de computadores.</li>
            <li>Elaborar, implementar e monitorar projetos de sistemas web.</li>
            <li>Elaborar e avaliar projetos de empreendedorismo na área da informática.</li>
            <li>Elaborar projetos de pesquisa aplicando metodologia científica.</li>
            <li>Identificar os riscos ocupacionais do ambiente de trabalho.</li>
            <li>Conhecer os conceitos de legislação básica do direito utilizados na profissão de informática.</li>
        </ul>

