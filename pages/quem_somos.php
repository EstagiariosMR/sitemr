<h1>Quem Somo?</h1>

<br>

<p>A Escola Estadual de Ensino Médio Professora Maria Rocha enriquece a cada dia a sua história em prol da educação. Enobrece e contribui para a cultura de nossa cidade há mais de 80 anos.</p>

<p> Tem apoiado significativamente a formação integral dos jovens. A criatividade, autonomia e a pesquisa, através de uma prática educativa atualizada e assumida pelos profissionais que atuam, e pelos que já atuaram nessa nobre instituição de ensino. </p>

<p> A Escola, graças ao dinamismo, dedicação e interesse das pessoas que nela trabalham e ao êxito dos nossos alunos nos bancos universitários e no campo profissional, se projeta no cenário cultural de nossa cidade.</p>

<p>img</p>

<p> A Escola busca além de excelência acadêmica, a formação dos jovens em todas as dimensões ética, espiritual, física, afetiva, valorativa, além de possibilitar aos alunos, a integração ao mundo contemporâneo nos aspectos fundamentais da cidadania e do trabalho. </p>

<p>A Escola, muito atenta às necessidades da comunidade no que diz respeito ao mercado de trabalho oferta, também, o Ensino Profissionalizante - em nível Pós-Médio - com os cursos técnicos em Contabilidade, Informática e Secretariado. Oportuniza aos alunos a ampliação de oportunidades de atuação na vida moderna, preparando-os para o mundo tecnológico e científico, integrando-os ao trabalho e proporcionando o desenvolvimento individual e interpessoal, aproximando a Escola do mundo real. Assim, há empenho de toda a comunidade escolar no sentido de formar jovens preparados pra atuar profissionalmente, na sociedade com competência e qualidade. </p>

<p>img</p>

<p>A Escola Estadual de Ensino Médio Professora Maria Rocha completou, em 2021, 80 anos de atividades educacionais. Está situada na rua Conde de Porto Alegre, nº 795, na cidade de Santa Maria.</p>

<h1>Nossa escola funciona nos três turnos - manhã, tarde e noite - e possui três modalidades de ensino:</h1>
<ul>
    <li>Ensino Médio;</li>
    <li>Curso Técnico em Informática Integrado ao Ensino Médio;</li>
    <li>Cursos Técnicos Subsequentes em Contabilidade, Informática e Secretariado.</li>

</ul>

