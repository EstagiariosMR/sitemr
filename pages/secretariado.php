        <h2>Objetivo do Curso</h2>
        <p>
            O Curso Técnico em Secretariado forma um profissional que atue com dinamismo, gerenciando as mais diversas situações, de maneira compreensível, 
			ética e responsável, bem como assessore diferentes setores e pessoas, coordene equipes, gere fluxo de informações da empresa e atue junto ao público interno e externo. 
        </p>

        <h2>Critérios de Avaliação</h2>
        <p>
            A avaliação considerada como processo de construção do conhecimento combina procedimentos de avaliação de
            competências que se realiza de forma contínua, envolvendo teoria e prática. O resultado é expresso pelos pareceres
            Apto e Não Apto. Quando o aluno, após as atividades de recuperação desenvolvidas durante o processo for considerado
            Não Apto, a escola oferece uma nova oportunidade, sendo retomados os conteúdos mínimos que propiciem aquisição de
            conhecimentos básicos para promoção da etapa seguinte: os estudos adicionais. No período de estudos adicionais,
            são retomadas as competências significativas para a promoção do aluno.
        </p>
		
		 <p>
            A Escola exige do aluno o percentual de 75% de frequência nas atividades curriculares, conforme legislação vigente,
            oferecendo-lhe atividades compensatórias e presencias de infrequência.
        </p>

        <h2>Estágio</h2>
        <p>
            O estágio curricular obrigatório oportuniza a vivência da integração entre a teoria e a prática fora do ambiente
            pedagógico. É realizado no quarto semestre do curso, em empresa ou propriedade do setor público ou privado que
            exija ou necessite de profissional na área de Informática. O estágio é realizado sob a orientação do professor da
            disciplina de orientação de Estágio e deve ter a duração de 200 horas.
        </p>

        <h2>Perfil Profissional de Conclusão</h2>
        <p>
            
        </p>
        <p>
           O Técnico em Secretariado atende ao público e assessora chefias em empresas públicas, privadas e do terceiro setor. Utiliza tecnologias e 
			comunicação nas diversas formas, inclusive em língua estrangeira. Organiza arquivos e sistematiza documentos técnicos. Atuam no planejamento, 
			execução e avaliação de eventos.
			É um profissional ético, preocupado com o meio ambiente, proativo e responsável, trabalha em equipe, valoriza a produtividade e qualidade no local de 
			trabalho, mantendo postura adequado no exercício da profissão.
        </p>
        <p>
            O Técnico em Secretariado terá com competências: Viabilizar aprendizagem de conhecimentos e competências essenciais para o exercício da profissão, 
			investir no uso de tecnologias educacionais que remetem o aluno às mudanças continuas das áreas de atuação. Formar profissionais capazes de interagir 
			nas diversas situações do mundo do trabalho. Exercer funções gerenciais, condomínio sobre planejamento, organização, direção e controle.Proporcionar 
			condições de alto desenvolvimento no Eixo Tecnológico Gestão e Negócios, propondo inovações para incorporar novos métodos e técnicas que respondam às
 situações cotidianas e inusitadas com criatividade, assertividade, flexibilidade e adaptabilidade.Proporcionar a todas as áreas de Santa Maria e 
 região melhores condições de atendimento ao cliente, colocando-lhes a disposição profissional tecnicamente qualificados.Conhecer os conteúdos básicos 
 do direito, bem como trabalhar a legislação civil, empresarial, trabalhista, previdenciária e constitucional numa perspectiva de apoio aos serviços de
 secretariado. Fornecer alimentos técnico-jurídicos para o exercício pleno da profissional
 e vida social critica Tornar um profissional ético, proativo, responsável, com uma visão holística, flexível e de bons relacionamentos interpessoais. 
        </p>
