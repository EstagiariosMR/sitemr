        <h2>Objetivo do Curso</h2>
        <p>
            O Curso Técnico em Contabilidade objetiva desenvolver no aluno a excelência contábil, de maneira ética e organizada, 
			transformando as informações em conhecimentos técnicos e operacionais inerentes à área de gestão e negócios.
        </p>

        <h2>Critérios de Avaliação</h2>
        <p>
            A avaliação considerada como processo de construção do conhecimento combina procedimentos de avaliação de competências que se 
			realiza de forma contínua, envolvendo teoria e prática.
			O resultado é expresso pelos pareceres Apto e Não Apto.
			Quando o aluno, após as atividades de recuperação desenvolvidas durante o processo for considerado Não Apto, a escola oferece uma nova oportunidade, 
			sendo retomados os conteúdos mínimos que propiciem aquisição de conhecimentos básicos para promoção da etapa seguinte: os estudos adicionais. 
			No período de estudos adicionais, são retomadas as competências significativas para a promoção do aluno.
			A Escola exige do aluno o percentual de 75% de frequência nas atividades curriculares, conforme legislação vigente, oferecendo-lhe atividades compensatórias e presencias de infrequência. 
        </p>

